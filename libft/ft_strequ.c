#include "libft.h"

int	ft_strequ(char const *s1, char const *s2)
{
	if (!s1 && !s2)
		return (0x1);
	if (s1 && s2)
		return (ft_strcmp(s1, s2) == 0x0 ? 0x1 : 0x0);
	return (0x0);
}

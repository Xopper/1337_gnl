#include "libft.h"

int	ft_strnequ(char const *s1, char const *s2, size_t n)
{
	if (!s1 && !s2 && !n)
		return (0x1);
	if (s1 && s2)
		return (ft_memcmp(s1, s2, n) == 0x0 ? 0x1 : 0x0);
	return (0x0);
}
